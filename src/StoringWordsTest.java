import static org.junit.jupiter.api.Assertions.*;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;


class StoringWordsTest {

    private StoringWords storingWords;

    @BeforeEach
    void setup(){
        storingWords = new StoringWords(new String[]{"chelsea", "arsenal"});
    }

    @Test
     void getRandomWord() {
        String randomWord = storingWords.getRandomWord();
        assertTrue(randomWord.equals("chelsea") || randomWord.equals("arsenal"));
    }
}